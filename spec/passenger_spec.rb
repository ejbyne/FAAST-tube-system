require 'passenger'

describe Passenger do
  
  it "should be able to touch in at a station"

  it "should be able to touch out at a station"

  it "should not be able to touch in at a station if the station is full of passengers"

  it "should not be able to touch in at a station if he/she does not have at least GBP 2 credit"

  it "should be able to board a carriage of a train which is at the same station as the passenger"

  it "should be able to alight a carriage of a train which is at a station"

  it "should not be able to board a carriage if the carriage is full of passengers"

  it "should not be able to board a carriage if he/she does not have at least GBP 2 credit"

end